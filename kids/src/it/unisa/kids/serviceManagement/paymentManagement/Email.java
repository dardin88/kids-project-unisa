package it.unisa.kids.serviceManagement.paymentManagement;

public class Email {
	
	private String email;
	private String body;
	
	public Email() {
		
	}
	
	public String getEmail() {
		return this.email;
	}
	
	public void setEmail(String pEmail) {
		this.email = pEmail;
	}
	
	public String getBody() {
		return this.body;
	}
	
	public void setBody(String pBody) {
		this.body = pBody;
	}
}
